	internal static string GetTranslation(string phrase)
	{
		if (Locale == "en-US")
		{
			return phrase;
		}

		if (Locales.TryGetValue(Locale, out Dictionary<string, string> translations))
		{
			if (translations.TryGetValue(phrase, out string translation))
			{
				return translation;
			}
			else
			{
				Debug.Message += $"Missing '{Locale}' translation for '{phrase}'\n";
			}
		}
		else
		{
			Debug.Message += $"Missing translations for '{Locale}'\n";
		}

		return phrase;
	}
